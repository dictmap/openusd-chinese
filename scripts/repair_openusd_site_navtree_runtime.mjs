import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import vm from "node:vm";

const root = process.cwd();
const siteDir = path.join(root, "site");
const inventoryPath = path.join(root, "reports", "all_pages_inventory.json");
const navtreeDataPath = path.join(siteDir, "navtreedata.js");
const navtreeIndexPath = path.join(siteDir, "navtreeindex0.js");
const localNavtreePath = path.join(siteDir, "openusd_local_navtree.js");

const apiBase = "https://openusd.org/release/api/";
const sitePages = new Set([
  "site/index.html",
  "site/_usd__overview_and_purpose.html",
  "site/usd_page_front.html",
]);

function toSiteRelative(localOutput) {
  if (sitePages.has(localOutput)) {
    return localOutput.replace(/^site\//, "");
  }
  if (localOutput.startsWith("full_site/")) {
    return `../${localOutput}`;
  }
  return localOutput;
}

function splitHref(href) {
  const raw = String(href || "");
  const index = raw.indexOf("#");
  if (index === -1) return { target: raw, hash: "" };
  return { target: raw.slice(0, index), hash: raw.slice(index) };
}

function localRouteForHref(href, routes) {
  if (!href || /^(https?:|mailto:|javascript:)/i.test(href)) return href;
  const { target, hash } = splitHref(href);
  if (!target || !target.endsWith(".html")) return href;
  const normalizedTarget = target.replace(/^\.\//, "");
  const local = routes.get(normalizedTarget);
  if (local) return `${local}${hash}`;
  const official = `${apiBase}${normalizedTarget}${hash}`;
  return `uncovered_openusd_page.html?official=${encodeURIComponent(official)}`;
}

function cloneTreeWithRoutes(nodes, routes) {
  return nodes.map((node) => {
    const [label, href, children] = node;
    const routedHref = localRouteForHref(href, routes);
    const routedChildren = Array.isArray(children)
      ? cloneTreeWithRoutes(children, routes)
      : children;
    return [label, routedHref, routedChildren];
  });
}

function collectBreadcrumbs(nodes, out, trail = []) {
  nodes.forEach((node, index) => {
    const nextTrail = [...trail, index];
    const [, href, children] = node;
    if (href) {
      const originalTrail = nextTrail[0] === 0 ? nextTrail.slice(1) : nextTrail;
      out[href] = originalTrail;
      const { target, hash } = splitHref(href);
      if (hash) {
        out[target] ??= originalTrail.slice(0, -1);
      }
    }
    if (Array.isArray(children)) {
      collectBreadcrumbs(children, out, nextTrail);
    }
  });
}

function buildRouteMap(inventory) {
  const routes = new Map();
  for (const page of inventory.pages || []) {
    if (!page?.official_url || !page?.local_output) continue;
    const url = new URL(page.official_url);
    const prefix = "/release/api/";
    if (!url.pathname.startsWith(prefix)) continue;
    const key = decodeURIComponent(url.pathname.slice(prefix.length));
    routes.set(key, toSiteRelative(page.local_output));
  }
  return routes;
}

function buildLocalNavtreeScript(routes) {
  const routeObject = Object.fromEntries([...routes.entries()].sort(([a], [b]) => a.localeCompare(b)));
  return `/* Generated by scripts/repair_openusd_site_navtree_runtime.mjs. */
(function () {
  const routes = ${JSON.stringify(routeObject, null, 2)};
  const apiBase = ${JSON.stringify(apiBase)};

  function splitHref(href) {
    const index = href.indexOf("#");
    if (index === -1) return { target: href, hash: "" };
    return { target: href.slice(0, index), hash: href.slice(index) };
  }

  function routeHref(href, label) {
    if (!href || /^(https?:|mailto:|javascript:)/i.test(href)) return href;
    if (href.startsWith("../full_site/") || href.startsWith("uncovered_openusd_page.html")) return href;
    const parts = splitHref(href);
    if (!parts.target || !parts.target.endsWith(".html")) return href;
    const key = parts.target.replace(/^\\.\\//, "");
    if (routes[key]) return routes[key] + parts.hash;
    const official = apiBase + key + parts.hash;
    const title = label ? "&title=" + encodeURIComponent(label) : "";
    return "uncovered_openusd_page.html?official=" + encodeURIComponent(official) + title;
  }

  function rewriteNavtreeLinks() {
    document.querySelectorAll("#nav-tree a[href]").forEach((anchor) => {
      if (anchor.dataset.localRouteChecked === "true") return;
      const original = anchor.getAttribute("href");
      const routed = routeHref(original, anchor.textContent.trim());
      anchor.dataset.localRouteChecked = "true";
      if (routed !== original) {
        anchor.dataset.officialHref = apiBase + splitHref(original).target + splitHref(original).hash;
        anchor.dataset.localRoute = routed.startsWith("uncovered_openusd_page.html") ? "uncovered" : "mapped";
        anchor.setAttribute("href", routed);
      }
    });
  }

  document.addEventListener("DOMContentLoaded", () => {
    rewriteNavtreeLinks();
    const tree = document.getElementById("nav-tree");
    if (!tree) return;
    new MutationObserver(rewriteNavtreeLinks).observe(tree, { childList: true, subtree: true });
  });
})();
`;
}

